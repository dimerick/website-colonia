<h2>SAN LUIS ANTIOQUIA HISTORIA Y  GENERALIDADES</h1>
                
<p>
    San Luis es un municipio de Colombia, localizado en la subregión Oriente del departamento de Antioquia. Limita por el norte con los municipios de San Carlos y Puerto Nare, por el este con el municipio de Puerto Triunfo, por el sur con el municipio de San Francisco y por el oeste con los municipios de Cocorná y Granada.
    <br>
    Fue Fundado el 25 de agosto de 1876 por el padre Clemente Giraldo párroco en ese entonces de Vahos hoy municipio de Granada.  Su temperatura media es de 24 grados Celsius. Su cabecera municipal está a 124 kilómetros de la ciudad de Medellín, capital del Departamento de Antioquia, y el municipio posee una extensión de 453 km².  Cuenta con un corregimiento llamado El Prodigio y con 48 veredas. 
    <br>
    El Municipio de San Luis posee una predominancia económica en el sector agrícola, con una fortaleza en los cultivos de café y caña panelera, con mucha importancia, cacao, maíz, frutales de clima medio y caliente, en menor medida y transitoria de pan coger, con cultivos de aguacate, plátano, banano bocadillo y yuca. 
    <br>
    La caña panelera, siendo esta de gran importancia ya que cuenta con 65 trapiches artesanales y seis comunitarios, estos último cumplen con estándares de alta calidad y con diversificación de la producción, ya no sólo se produce en bloque, sino que se ha impulsado la producción de panela pulverizada y granulada Se están desarrollando productos de producción 100% natural y orgánicos, en productos como el café, cacao y panela, esto trabajado por organizaciones campesinas de la misma localidad
    <br>
    Se lo conoce con los apelativos de "Fortín Maderero" y "Perla Verde de Oriente".  De allí que su principal festividad lleve el nombre de “Fiestas de la madera” En otrora este municipio abastecía a gran parte de Los Municipios cercanos de Medellín de Madera. Sin embargo esta al ser un recurso no renovable. Poco a poco fue desgastando los recursos y economía local. Por ello ahora se realza un espíritu de conservación más ecológico conservando sus fuentes naturales y biodiversidad.
    <br>
    Hoy el municipio apuesta por el ecoturismo como uno de los principales renglones sociales y de economía por su agradable clima y gran biodiversidad, sus ríos, charcos, árboles gigantes y un paisaje hermoso, para vivir una gran experiencia que puede atraer a los visitantes.
    <br>
    Está ubicado en medio de un ecosistema de gran biodiversidad con muchas especies silvestres, algunas de ellas endémicas. Las piscinas naturales y las cascadas que forma el río Dormilón son muy apreciadas por los turistas y visitantes.  Además de las grutas cavernas propias del cinturón kárstico para un turismo de aventura. También hay hallazgos arqueológicos como petroglifos, metates, pictogramas, cerámicas y demás hallazgos de gran importancia histórica en la región.
</p>
