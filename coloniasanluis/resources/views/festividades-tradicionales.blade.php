<h2>FESTIVIDADES Y CELEBRACIONES RELIGIOSAS </h2>
<br>
<h3>SEMANA SANTA </h3>
<p>
    El municipio de San Luis es una región que por su cultura e idiosincrasia antioqueña tiene sus raíces profundamente marcadas dentro de la religión y en mayor medida frente al cristiano católico. La festividad mayor es la Semana Santa que en nuestro municipio se celebra multitudinariamente con procesiones por las calles del pueblo, ceremonias especiales y sobre todo un gran respeto y fervor aún de los que poco practican la religión. 
    <br>
    Desde el día domingo de ramos se vinculan a las celebraciones la comunidad y las instituciones conmemorando la vida, pasión, muerte y resurrección de Jesús, durante toda esa semana se da la celebración de varias eucaristías y en las horas de la tarde procesión por algunas calles del municipio finalizando el domingo de resurrección.
    <br>
    La semana mayor es una oportunidad de reencuentros con la familia, amigos, paisanos, incluso de otras regiones, que en medio de un ambiente tranquilo que nos llama a la reflexión y encuentro con Dios.
</p>
<br>
<h3>FIESTAS PATRONALES DE  SAN LUIS GONZAGA </h3>
<p>
    El 21 de junio es fiesta de San Luis Gonzaga, quien es el santo por el que el municipio lleva su nombre, aunque si bien el municipio fue fundado un 25 de agosto de 1872, los primeros colonizadores prefirieron elegir a San Luis Gonzaga como Santo patrono y no a San Luis Rey de Francia, principalmente por el fervor que le tenían al primero, además de sus virtudes como patrono de la juventud y protector de los jóvenes estudiantes. 
    <br>
    Esta festividad Se realizan en la tercera semana de junio iniciando el día 21; la Parroquia San Luis Gonzaga lleva a cabo diversas actividades donde se vinculan todas las instituciones tanto públicas como privadas del municipio, teniendo como actividades la novena y procesiones por todos los sectores del pueblo en honor a San Luis Gonzaga.
</p>
