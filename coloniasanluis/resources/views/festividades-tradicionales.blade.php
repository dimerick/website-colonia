<h2>FESTIVIDADES TRADICIONALES DE SAN LUIS ANTIOQUIA</h1>
    <br>
                
<h3>FIESTAS FOLCLÓRICAS Y RECREATIVAS, CULTURALES Y RECREATIVAS DEL CAMPESINO Y LA MADERA</h3>
<p>
En octubre 4 de 1970 por acuerdo N. 31, Fueron creadas las Fiestas de la Madera como evento tradicional de la municipalidad, estas fiestas  se institucionalizaron por iniciativa del señor alcalde de ese entonces Néstor  Gómez, con el objetivo de reunir todo el municipio una vez  al año en torno actividades Deportivas Culturales, Artísticas.
<br>
Sus organizadores han hecho énfasis en las actividades folclóricas y culturales, especialmente a las relacionadas con la promoción de nuestros valores y tradiciones. En estas fiestas se realizan eventos musicales, danzas, teatro, aserrío, zanquearos, juegos entre otros.
<br>
Estas festividades han tenido un impacto regional y nacional, se realizan gracias al apoyo de las empresas privadas que patrocinan orquestas de reconocida trayectoria a nivel departamental y nacional.
<br>
También se realizan actividades tradicionales del municipio como: carros de rodillos, la vara de premios, las pilanderas, bebes gateadores, concurso de sancochos, entre otros, donde se conoce el talento de los Sanluisanos.
<br>
Actualmente estas fiestas se realizan año tras año en el puente del mes de  junio y son reconocidas por personas de otros pueblos y ciudades, por la promoción que le hacen personas del municipio que están por fuera del el por diversas circunstancias.
</p>

<p>
    En el  2016  por acuerdo  del concejo municipal se dio nombre a este evento como  las fiestas Culturales, Deportivas y Recreativas del Campesino y la Madera con el fin de resaltar la labor del campesino y promover una cultura de conservación de los recursos naturales y ancestrales; realizando además muestras culturales, artísticas y agropecuarias, con el fin de  rescatar los saberes y tradiciones ancestrales.
</p>

<br>
<h3>ENCUENTRO CULTURAL Y DEPORTIVO DE COLONIAS SANLUISANAS</h3>
<p>
    En marzo  de 2003, por acuerdo entre todos los paisanos de San Luís residentes en las ciudades de Medellín, Cali, Buenaventura, Barranquilla y Caucasia, se tomó la determinación de celebrar el ENCUENTRO DE LAS COLONIAS, en la Perla Verde del Oriente, con los siguientes objetivos: Reunir a todos los Sanluisanos de todo el país, en un espacio de reencuentro, convivencia e integración. Promover las actividades folclóricas, culturales, deportivas, recreativas y cívicas, fomentando la hospitalidad y la fraternal convivencia. Ahora es evento tradicional, que se ha venido celebrando sin falta desde su inicio, se desarrolla en el mes de enero  durante el puente de reyes de cada año
    <br>
    Es importante recordar, que el fin principal de este encuentro de colonias es integrar a la comunidad de San Luis, no sólo a los que viven afuera sino a los sanluisianos que habitan en el municipio.
    <br>
    Durante este evento, se realiza un encuentro deportivo en la cancha de fútbol, ubicada en la parte baja del área urbana del municipio, éste es un torneo de todas las colonias de San Luis que están en el país, en el cual cada ciudad conforma y trae su equipo a competir con las otras colonias asistentes, por lo general se realiza del día jueves al domingo, durante el puente de reyes en el mes de enero.
    <br>
    Normalmente, el torneo se conforma de 8 a 10 equipos participantes, entre ellos, están las ciudades de: Cali, Buenaventura, Barraquilla, Santa Marta, Cartagena, Valledupar, Medellín, Doradal, Villavicencio y Sincelejo.
    <br>
    En este caso, son las propias colonias las que llegan  a jugar, por lo cual, buscan su patrocinio, uniformes, entre otros elementos.
</p>

<br>
<h3>FIESTAS DEL RETORNO LA AMISTAD Y EL TURISMO (CORREGIMIENTO EL PRODIGIO)</h3>
<p>
    En el corregimiento el prodigio de San Luis durante el mes de enero se celebra las fiestas del retorno y la amistad por iniciativa de sus habitantes como una festividad que evoca el reencuentro y los lazos de la gente del prodigio, a través de su cultura e historia en esta región cercana al magdalena medio. Se realizan actividades culturales, presentaciones musicales y muestras en lo referente a la ganadería donde en El Prodigio representa un importante renglón de la economía. Es también la oportunidad para conocer y disfrutar de sus charcos en la zona de las confusas y también visitar el corredor kárstico con sus grutas, cavernas y hallazgos arqueológicos. La temperatura cálida de El prodigio y su gente prende la Fiesta a la que todos están invitados.
</p>

<br>
<h3>EFEMÉRIDES SANLUISANAS (FUNDACIÓN DE SAN LUIS) </h3>

<p>
    San Luis rememora y celebra su historia el 25 de agosto de cada año con una conmemoración y ofenda a sus fundadores principalmente el padre “Clemente Giraldo” quien por su iniciativa y espíritu fue el principal promotor para que colonos del entonces municipio de Vahos (hoy Granada) divisaran un 25 de agosto de 1876 desde el cerro el Castellón, el territorio de lo que hoy es el casco urbano de San Luis.
    <br>
    Es una festividad con un énfasis en lo histórico y cultural celebrando en torno a la música, danza y expresiones literarias compuestas por niños, jóvenes y adultos. Asi como toda la comunidad en general 
    <br>
    Es de resaltar que estas últimas celebraciones  la alcaldía Municipal hace una reconocimiento, aquellos líderes del municipio que de una u otra forma han trazado la historia de nuestro  municipio.
</p>

